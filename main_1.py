N = int(input())
K = int(input())
M = int(input())

# Проверяем, что K больше M, иначе детали не могут быть выточены
if K <= M:
    print("Ошибка: масса заготовки должна быть больше массы детали.")
else:
    total_details = 0  # Общее количество деталей

    while N >= K:  # Пока есть достаточно сплава для изготовления хотя бы одной заготовки
        # Изготавливаем заготовки
        num_blanks = N // K  # Количество заготовок
        leftover_after_blanks = N % K  # Остаток сплава после изготовления заготовок

        # Изготавливаем детали из заготовок
        num_details_per_blank = K // M  # Количество деталей из одной заготовки
        total_details += num_blanks * num_details_per_blank  # Обновляем общее количество деталей
        leftover_after_details = K % M  # Остаток сплава после вытачивания деталей из одной заготовки

        # Обновляем количество сплава для следующей итерации
        N = leftover_after_blanks + (num_blanks * leftover_after_details)

    # Выводим общее количество деталей
    print("Общее количество деталей:", total_details)